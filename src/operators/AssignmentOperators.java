package operators;

public class AssignmentOperators 
{
	public static void main(String[] args) 
	{
		int a=15;
		int b=9;
		
		System.out.println("a+=b :="+(a+=b)+" ,here the total value is stored in a");
		System.out.println("a-=b :="+(a-=b)+" ,here the total value is stored in a");
		System.out.println("a*=b :="+(a*=b)+" ,here the total value is stored in a");
		System.out.println("a/=b :="+(a/=b)+" ,here the total value is stored in a");
		System.out.println("a%=b :="+(a%=b)+" ,here the total value is stored in a");
	}

}
